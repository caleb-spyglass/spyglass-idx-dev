// ─── HELPER: Extract zip code from a slug ────────────────────────────────

/**
 * If a slug looks like a zip-code URL (starts with 5 digits followed by a hyphen),
 * extract and return the zip code. Otherwise return null.
 */
export function extractZipFromSlug(slug: string): string | null {
  const match = slug.match(/^(\d{5})-/);
  if (match) return match[1];
  // Also handle bare 5-digit slugs
  if (/^\d{5}$/.test(slug)) return slug;
  return null;
}

